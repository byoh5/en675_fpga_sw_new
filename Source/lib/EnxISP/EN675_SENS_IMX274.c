void InitSensRun(void)
{
	SetSens(0x3000, 0x12);

	for(UINT i=0;i<ARRAY_SIZE(gwTblIMX274); i++)
	{
		SetSens(gwTblIMX274[i][0],gwTblIMX274[i][1]);
		//WaitXus(200);
	}

	WaitXus(10000);				//wait 10ms

	SetSens(0x3000, 0x00);
	SetSens(0x303E, 0x02);

	WaitXus(7000);				//wait 7ms

	//Setting 3
	SetSens(0x30F4, 0x00);
	SetSens(0x3018, 0xA2);
}

